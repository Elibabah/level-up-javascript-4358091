class TechnicalBook extends Book {
  constructor(title, author, ISBN, numCopies, edition) {
    super(title, author, ISBN, numCopies);
    this.edition = edition;
  }

  get editionBook() {
    return this.getEdition();
  }

  getEdition() {
    return this.edition;
  }
}

